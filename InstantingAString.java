public class InstantingAString {

    public static void main(String[] args) {
        String name1="Imtiaz Academy";
        String name2="Imtaiz Academy";
        String name3= new String("Imtiaz Academy");

        System.out.println(name1);
        System.out.println(name2);

        //Instanting a string 
        System.out.println(name3);
    }
}