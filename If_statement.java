public class If_statement {
    public static void main(String[] args) {
        int x=5;
        int y=10;
        if(x>y){
            System.out.println("The maximum is x: "+x);
        }
        if(x<y){
            System.out.println("The maximum is y: "+y);
        }
        if(x==y){
            System.out.println("The numbers are equal.");
        }
    }
}