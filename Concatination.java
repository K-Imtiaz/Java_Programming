public class Concatination {
    public static void main(String[] args) {
        // This is bigger concatination.
        String p1="My name is Imtiaz Ahmed";
        String p2="And I am a student";
        String p3="I am very lazy";
        String p4=p1+"."+p2+"."+p3+".";
    
        System.out.println(p4);
        System.out.println(p4+"My favorite number is "+5);

//this is shorter concatination.
//concatination makes very easy to make java easier.
        System.out.println("Imtiaz".concat(" ").concat("is").concat(" ").concat("Good").concat(" ").concat("boy"));
    }
    
}
